def find_city_by_name(user_input):
    """
    Find city from user input (case-insensitive, supports variations)
    Returns city_key, city_data or None, None if not found
    """
    user_input_lower = user_input.lower().strip()
    
    # Exact match with variations
    for city_key, city_data in TOURIST_PLACES_DB.items():
        variations = get_city_name_variations(city_key)
        for variation in variations:
            if variation in user_input_lower or user_input_lower in variation:
                return city_key, city_data
    
    # Partial match
    for city_key, city_data in TOURIST_PLACES_DB.items():
        if city_key in user_input_lower:
            return city_key, city_data
    
    return None, None

def get_formatted_places_response(city_key, city_data):
    """
    Return city name + description + instruction text + place names
    Format: "CityName is Description\n\n🎯 Choose from these places where you want to visit:"
    """
    city_name = city_data.get('name', city_key.capitalize())
    city_description = city_data.get('description', 'A wonderful destination')
    
    # Include city name and description
    response_text = f"<b>{city_name}</b> is {city_description}\n\n🎯 <b>Choose from these places where you want to visit:</b>"
    
    # Extract just the place names - NO descriptions
    places_list = [place['name'] for place in city_data['places']]
    
    return response_text, places_list


def get_place_details(city_key, place_name):
    """
    Get place details including name and description
    """
    if city_key not in TOURIST_PLACES_DB:
        return None
    
    city_data = TOURIST_PLACES_DB[city_key]
    places = city_data.get('places', [])
    
    for place in places:
        if place['name'].lower() in place_name.lower() or place_name.lower() in place['name'].lower():
            return {
                'name': place['name'],
                'description': place.get('description', 'No description available')
            }
    
    return None
